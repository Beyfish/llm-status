// ChatGPT Next Web - exports as .env file
export async function exportChatGptNextWeb(data: ExportData): Promise<string> {
  const envLines = data.providers
    .filter((p) => p.apiKey)
    .map((p) => {
      const keyUpper = p.type.toUpperCase().replace(/-/g, '_');
      return `OPENAI_API_KEY=${p.apiKey}\nOPENAI_BASE_URL=${p.baseUrl || 'https://api.openai.com'}`;
    })
    .join('\n');

  const envContent = `# ChatGPT Next Web Configuration\n# Generated by LLM Status\n\n${envLines}\n`;

  const result = await dialog.showSaveDialog({
    title: 'Export to ChatGPT Next Web',
    defaultPath: '.env',
    filters: [{ name: 'Environment', extensions: ['env'] }],
  });

  if (result.filePath) {
    writeFileSync(result.filePath, envContent, 'utf-8');
    return result.filePath;
  }
  throw new Error('Export cancelled');
}

