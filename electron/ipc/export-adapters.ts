import { writeFileSync } from 'fs';
import { dialog } from 'electron';

interface ExportData {
  providers: Array<{
    name: string;
    type: string;
    baseUrl?: string;
    apiKey?: string;
    models?: string[];
  }>;
}

// Cherry Studio - exports as JSON config
export async function exportCherryStudio(data: ExportData): Promise<string> {
  const cherryConfig = {
    providers: data.providers.map((p) => ({
      id: p.type,
      name: p.name,
      apiKey: p.apiKey || '',
      apiHost: p.baseUrl || '',
      models: p.models || [],
    })),
  };

  const result = await dialog.showSaveDialog({
    title: 'Export to Cherry Studio',
    defaultPath: 'cherry-studio-config.json',
    filters: [{ name: 'JSON', extensions: ['json'] }],
  });

  if (result.filePath) {
    writeFileSync(result.filePath, JSON.stringify(cherryConfig, null, 2), 'utf-8');
    return result.filePath;
  }
  throw new Error('Export cancelled');
}

